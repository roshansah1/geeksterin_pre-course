public class IFElseIF {

	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 300;
		int num3 = 900;
		
		if (num1>num2 && num1>num3)
			System.out.println(num1);
		
			else if (num2>num1 && num2>num3)
				System.out.println(num2);
		
			else 
				System.out.println(num3);
		
		
	}

}
